//整型提升举例
int main(){
	//c只要参与表达式运算，就会发生整型提升，表达式 +c 就会发生提升，所以sizeof(+c)是4个字节
	//表达式 -c 也会发生整型提升，所以sizeof(-c) 是4个字节，
	//但是sizeof(c)是一个字节
	char c = 1;
	printf("%u\n",sizeof(c));	  //1
	printf("%u\n",sizeof(+c));	  // 操作符 + - 则+4 是一个表达式，则会发生整型提升 4
	printf("%u\n",sizeof(-c));	  //4
	return 0;
}

